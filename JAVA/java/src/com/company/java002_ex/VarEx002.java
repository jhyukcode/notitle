package com.company.java002_ex;

public class VarEx002 {

	public static void main(String[] args) {
		int a; int b;
		a = 10; b = 3;
		System.out.println(a+"+"+b+"="+(a+b));
		System.out.println(a+"-"+b+"="+(a-b));
		System.out.println(a+"*"+b+"="+(a*b));
		System.out.println(a+"/"+b+"="+(a/b));
		System.out.printf(a+"/"+b+"="+"%.2f", (float)a/b);
	}

}
